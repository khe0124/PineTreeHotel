package kr.co.pinetree.contact;

public class FaqDTO {
	private int fq_num;
	private String fq_title;
	private String fq_cont;
	private String fq_date;
	private int fq_visitnum;
	private String fq_type;
	public int getFq_num() {
		return fq_num;
	}
	public void setFq_num(int fq_num) {
		this.fq_num = fq_num;
	}
	public String getFq_title() {
		return fq_title;
	}
	public void setFq_title(String fq_title) {
		this.fq_title = fq_title;
	}
	public String getFq_cont() {
		return fq_cont;
	}
	public void setFq_cont(String fq_cont) {
		this.fq_cont = fq_cont;
	}
	public String getFq_date() {
		return fq_date;
	}
	public void setFq_date(String fq_date) {
		this.fq_date = fq_date;
	}
	public int getFq_visitnum() {
		return fq_visitnum;
	}
	public void setFq_visitnum(int fq_visitnum) {
		this.fq_visitnum = fq_visitnum;
	}
	public String getFq_type() {
		return fq_type;
	}
	public void setFq_type(String fq_type) {
		this.fq_type = fq_type;
	}
	
	
	
}//
